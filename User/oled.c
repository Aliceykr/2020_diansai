/* 在 (x,y) 写一个 6x8 字符（仅写显存，非可见 ASCII 用空格代替） */
void OLED_ShowChar(uint8_t x, uint8_t y, char ch)
{
    if (ch < ' ' || ch > '~')
        ch = ' ';
    ch -= ' ';

    for (uint8_t i = 0; i < 6; i++)
    {
        uint8_t line = Font6x8[ch][i];
        for (uint8_t j = 0; j < 8; j++)
        {
            if (line & (1 << j))
                OLED_DrawPixel(x + i, y + j, 1);
            else
                OLED_DrawPixel(x + i, y + j, 0);
        }
    }
}
/* 在 (x,y) 写字符串，每字符宽 6 像素，到右边界自动折到下一行 */
void OLED_ShowString(uint8_t x, uint8_t y, const char *str)
{
    while (*str)
    {
        OLED_ShowChar(x, y, *str++);
        x += 6;
        if (x + 6 > OLED_WIDTH)
        {
            x = 0;
            y += 8;
        }
    }
}


/* 在 (x,y) 格式化输出（用法同 printf） */
void OLED_Printf(uint8_t x, uint8_t y, const char *fmt, ...)
{
    char buf[32];
    va_list ap;

    va_start(ap, fmt);
    vsnprintf(buf, sizeof(buf), fmt, ap);
    va_end(ap);

    OLED_ShowString(x, y, buf);
}
